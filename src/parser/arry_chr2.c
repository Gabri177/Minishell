#include "../../include/parser.h"

int	is_not_key(char c)
{
	if (c != ' ' && c != '|' && c != '>' && c != '<'
		&& c != QUOTE_BIG && c != QUOTE_LIT)
		return (TRUE);
	return (FALSE);
}
